class ResumeRequest(BaseModel):
    data: dict
    format: str  # "pdf" or "docx"

@router.post("/resume")
async def build_resume(req: ResumeRequest):
    d = req.data
    name        = d.get('name', 'Your Name')
    title       = d.get('title', 'Professional')
    email       = d.get('email', '')
    phone       = d.get('phone', '')
    skills      = d.get('skills', '')
    experience  = d.get('experience', '')
    education   = d.get('education', '')
    achievements = d.get('achievements', '')

    if req.format == 'pdf':
        return await generate_resume_pdf(name, title, email, phone,
                                         skills, experience, education, achievements)
    else:
        return await generate_resume_docx(name, title, email, phone,
                                          skills, experience, education, achievements)

async def generate_resume_pdf(name, title, email, phone,
                               skills, experience, education, achievements):
    try:
        from reportlab.lib.pagesizes import A4
        from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
        from reportlab.lib.units import cm
        from reportlab.lib import colors
        from reportlab.platypus import (SimpleDocTemplate, Paragraph, Spacer, HRFlowable, Table, TableStyle)
        from reportlab.lib.enums import TA_LEFT, TA_CENTER

        buffer = io.BytesIO()
        doc = SimpleDocTemplate(buffer, pagesize=A4,
            leftMargin=2*cm, rightMargin=2*cm,
            topMargin=2*cm, bottomMargin=2*cm)

        ACCENT = colors.HexColor('#6366f1')
        DARK   = colors.HexColor('#111827')
        GRAY   = colors.HexColor('#6b7280')
        WHITE  = colors.white

        name_style = ParagraphStyle('Name', fontSize=26,
            fontName='Helvetica-Bold', textColor=WHITE,
            alignment=TA_LEFT, leading=32)
        title_style = ParagraphStyle('Title', fontSize=13,
            fontName='Helvetica', textColor=colors.HexColor('#c7d2fe'),
            alignment=TA_LEFT, spaceAfter=4)
        contact_style = ParagraphStyle('Contact', fontSize=9,
            fontName='Helvetica', textColor=colors.HexColor('#e0e7ff'),
            alignment=TA_LEFT)
        section_style = ParagraphStyle('Section', fontSize=12,
            fontName='Helvetica-Bold', textColor=ACCENT,
            spaceBefore=14, spaceAfter=6)
        body_style = ParagraphStyle('Body', fontSize=10,
            fontName='Helvetica', textColor=DARK,
            spaceAfter=5, leading=15)

        story = []

        # Header block with accent background
        header_data = [[
            Paragraph(name, name_style),
        ]]
        header_table = Table(header_data, colWidths=[17*cm])
        header_table.setStyle(TableStyle([
            ('BACKGROUND', (0,0), (-1,-1), ACCENT),
            ('PADDING', (0,0), (-1,-1), 16),
            ('ROUNDEDCORNERS', [8]),
        ]))
        story.append(header_table)
        story.append(Spacer(1, 4))

        # Contact row
        contact_info = []
        if email: contact_info.append(f"✉ {email}")
        if phone: contact_info.append(f"📞 {phone}")
        contact_str = "  |  ".join(contact_info) if contact_info else ""

        info_data = [[
            Paragraph(title, ParagraphStyle('T2', fontSize=12,
                fontName='Helvetica-Bold', textColor=DARK)),
            Paragraph(contact_str, ParagraphStyle('C2', fontSize=9,
                fontName='Helvetica', textColor=GRAY, alignment=2))
        ]]
        info_table = Table(info_data, colWidths=[9*cm, 8*cm])
        info_table.setStyle(TableStyle([
            ('PADDING', (0,0), (-1,-1), 6),
            ('BACKGROUND', (0,0), (-1,-1), colors.HexColor('#f8faff')),
            ('ROUNDEDCORNERS', [6]),
        ]))
        story.append(info_table)
        story.append(Spacer(1, 10))

        def add_section(heading, content):
            if not content or content.lower() == 'none': return
            story.append(Paragraph(heading, section_style))
            story.append(HRFlowable(width="100%", thickness=1,
                                    color=ACCENT, spaceAfter=6))
            # Split bullet items
            for line in content.split(','):
                line = line.strip()
                if line:
                    story.append(Paragraph(f"• {line}", body_style))

        def add_section_full(heading, content):
            if not content or content.lower() == 'none': return
            story.append(Paragraph(heading, section_style))
            story.append(HRFlowable(width="100%", thickness=1,
                                    color=ACCENT, spaceAfter=6))
            story.append(Paragraph(content, body_style))

        add_section("SKILLS", skills)
        add_section_full("EXPERIENCE", experience)
        add_section_full("EDUCATION", education)
        add_section("ACHIEVEMENTS", achievements)

        # Footer
        story.append(Spacer(1, 20))
        story.append(HRFlowable(width="100%", thickness=0.5,
                                color=colors.HexColor('#e5e7eb')))
        story.append(Paragraph("Resume generated by Tinku AI",
            ParagraphStyle('Footer', fontSize=8, textColor=GRAY,
                           alignment=TA_CENTER, spaceBefore=6)))

        doc.build(story)
        buffer.seek(0)
        return StreamingResponse(buffer, media_type="application/pdf",
            headers={"Content-Disposition": 'attachment; filename="resume.pdf"'})

    except Exception as e:
        return JSONResponse({"error": str(e)}, status_code=500)


async def generate_resume_docx(name, title, email, phone,
                                skills, experience, education, achievements):
    try:
        from docx import Document
        from docx.shared import Pt, RGBColor, Cm
        from docx.enum.text import WD_ALIGN_PARAGRAPH

        doc = Document()
        for section in doc.sections:
            section.top_margin = Cm(2)
            section.bottom_margin = Cm(2)
            section.left_margin = Cm(2)
            section.right_margin = Cm(2)

        # Name
        h = doc.add_heading(name, 0)
        h.runs[0].font.size = Pt(24)
        h.runs[0].font.color.rgb = RGBColor(0x63, 0x66, 0xf1)

        # Title + contact
        sub = doc.add_paragraph()
        sub.add_run(title).font.bold = True
        if email or phone:
            contact = []
            if email: contact.append(email)
            if phone: contact.append(phone)
            sub.add_run(f"  |  " + "  |  ".join(contact)).font.color.rgb = RGBColor(0x6b, 0x72, 0x80)

        doc.add_paragraph("─" * 60)

        def add_section(heading, content):
            if not content or content.lower() == 'none': return
            h2 = doc.add_heading(heading, level=1)
            h2.runs[0].font.color.rgb = RGBColor(0x63, 0x66, 0xf1)
            h2.runs[0].font.size = Pt(12)
            for item in content.split(','):
                item = item.strip()
                if item:
                    p = doc.add_paragraph(style='List Bullet')
                    p.add_run(item).font.size = Pt(10)

        def add_section_full(heading, content):
            if not content or content.lower() == 'none': return
            h2 = doc.add_heading(heading, level=1)
            h2.runs[0].font.color.rgb = RGBColor(0x63, 0x66, 0xf1)
            h2.runs[0].font.size = Pt(12)
            p = doc.add_paragraph(content)
            p.runs[0].font.size = Pt(10)

        add_section("SKILLS", skills)
        add_section_full("EXPERIENCE", experience)
        add_section_full("EDUCATION", education)
        add_section("ACHIEVEMENTS", achievements)

        doc.add_paragraph("─" * 60)
        footer = doc.add_paragraph("Resume generated by Tinku AI")
        footer.runs[0].font.size = Pt(8)
        footer.runs[0].font.color.rgb = RGBColor(0x6b, 0x72, 0x80)
        footer.runs[0].font.italic = True

        buffer = io.BytesIO()
        doc.save(buffer)
        buffer.seek(0)
        return StreamingResponse(buffer,
            media_type="application/vnd.openxmlformats-officedocument.wordprocessingml.document",
            headers={"Content-Disposition": 'attachment; filename="resume.docx"'})

    except Exception as e:
        return JSONResponse({"error": str(e)}, status_code=500)
